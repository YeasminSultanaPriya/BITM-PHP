    public function getNewsByCategoryId($categoryId){
        $this->newses = $this->getAllNews();
        foreach ($this->newses as $news){
            if ($news['category_id'] == $categoryId){
                array_push($this->result,$news);
            }

        }
        return $this->result;
    }
    public function getNewsDetails($newsId){
        $this->newses = $this->getAllNews();
        foreach ($this->newses as $news){
            if ($news['id']==$newsId){
                return $news;
            }

        }
    }
